                                                 
                                                 
                                                 //type casting program 


class TypeCasting
{
	public static void main(String[]args)
	{
	int a=10;
	float b=0;
	b=a;
		System.out.println("implicit type casting answer:"+b);
	float x=34.6785f;
	int y=(int)x+10;
		System.out.println("explicit type casting answer:"+y);
	}
	}
	
	
	
